class Sabai_Form_Element_SelectYesNo extends Sabai_Form_Element_SelectRadioButton
{
    function Sabai_Form_Element_SelectYesNo($name, $yesText = 'Yes', $noText = 'No', $yesValue = 1, $noValue = 0)
    {
        parent::Sabai_Form_Element_SelectRadioButton($name);
        $this->addOption($yesValue, $yesText);
        $this->addOption($noValue, $noText);
    }
